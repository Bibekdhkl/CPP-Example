//Program to find Maximum of 2 Numbers
#include <iostream>

using namespace std;//container where identifier like cin cout are defined

int main(){
    int i,j;
    cout<<"Enter Two Number";
    cin>>i>>j;
    if (i>j){
        cout<<i<<" is greatest";
    }
    else{
        cout<<j<<" is greatest";
    }
    return 0;
}
